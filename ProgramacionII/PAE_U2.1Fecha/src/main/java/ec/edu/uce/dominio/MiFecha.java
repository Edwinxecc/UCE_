package ec.edu.uce.dominio;

public class MiFecha {
    private int dia;
    private int mes;
    private int anio;

    public MiFecha(){
        this.dia = 1;
        this.mes = 1;
        this.anio = 1990;
    }

    public MiFecha (int dia, int mes, int anio){
        this.dia = dia;
        this.mes = mes;
        this.anio = anio;
    }

}
